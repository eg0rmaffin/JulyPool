int	ft_recursive_factorial(int nb)
{
	if (nb == 0)
	{
		return (1);
	}
	else if ((nb > 12) || (nb < 0))
	{
		return (0);
	}
	else
	{
		return (nb * ff(nb - 1));
	}
}
