int		ft_strcmp(char *s1, char *s2)
{
	int run;

	run = 0;
	while ((s1[run] == s2[run]) && (s1[run] != '\0') && (s2[run] != '\0'))
		run++;
	return (s1[run] - s2[run]);
}
