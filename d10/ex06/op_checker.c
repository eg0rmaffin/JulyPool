#include "header.h"

char	*op_checker(char *str)
{
	int run;

	run = 0;
	while (str[run] != '\0')
		++run;
	if ((run == 1) && ((str[0] == 47) || (str[0] == 37) ||
				(str[0] == 42) || (str[0] == 43) || (str[0] == 45)))
		return (str);
	else
		return ("z");
}
