#include "header.h"

int		calculator(int a, int b, char *op)
{
	if (op[0] == 47)
		return (a / b);
	if (op[0] == 37)
		return (a % b);
	if (op[0] == 42)
		return (a * b);
	if (op[0] == 43)
		return (a + b);
	if (op[0] == 45)
		return (a - b);
}
