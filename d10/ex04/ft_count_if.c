int		ft_count_if(char **tab, int (*f)(char*))
{
	int run;
	int subrun;

	run = 0;
	subrun = 0;
	while (tab[run] != '\0')
	{
		if ((*f)(tab[run]) == 1)
			++subrun;
		++run;
	}
	return (subrun);
}
